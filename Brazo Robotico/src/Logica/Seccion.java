package Logica;

public class Seccion {
   private int Ix;
   private int Iy;
   private int Fx;
   private int Fy;
   private int longitud;
   
   public Seccion(){
  
   }

    public int getIx() {
        return Ix;
    }

    public int getIy() {
        return Iy;
    }

    public int getFx() {
        return Fx;
    }

    public int getFy() {
        return Fy;
    }

    public void setIx(int Ix) {
        this.Ix = Ix;
    }

    public void setIy(int Iy) {
        this.Iy = Iy;
    }

    public void setFx(int Fx) {
        this.Fx = Fx;
    }

    public void setFy(int Fy) {
        this.Fy = Fy;
    }

    public void setLongitud(int longitud) {
        this.longitud = longitud;
    }

    public int getLongitud() {
        return longitud;
    }
    
}
